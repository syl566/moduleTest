public class Calculator {

public int addNumber(int x ,int y){
   int sum =  x + y;
   return sum;
}

public int subtraction(int x ,int y){
    int sum = x - y;
    return sum;
}
public int multiplication(int x, int y){
    int sum = x * y;
    return sum;
}
public int division(int x, int y){
    int sum = x / y;
    return sum;
}
}


